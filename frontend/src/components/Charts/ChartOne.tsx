interface ChartOneState {
  series: {
    name: string;
    data: number[];
  }[];
}

const ChartOne: React.FC = () => {
  const [state, setState] = useState<ChartOneState>({
    series: [
      {
        name: "Flights",
        data: Array(12).fill(0),
      },
    ],
  });

  useEffect(() => {
    const fetchData = async () => {
      try {
        const response = await fetchWithAuth(
          "http://127.0.0.1:5000/api/flights?per_page=1000",
        );
        const data = await response.json();
        const flights = data.flights;

        const flightsPerMonth = Array(12).fill(0);
        flights.forEach((flight: any) => {
          const month = parseInt(flight.date_time.substring(5, 7));
          console.log(month);
          flightsPerMonth[month - 1]++;
        });

        setState({
          series: [
            {
              name: "Flights",
              data: flightsPerMonth,
            },
          ],
        });
      } catch (error) {
        console.error("Error fetching flight data", error);
      }
    };

    fetchData();
  }, []);

  return (
    <div className="w-3/4 rounded-sm border border-stroke bg-white px-5 pb-5 pt-7.5 shadow-default dark:border-strokedark dark:bg-boxdark sm:px-7.5">
      <div className="flex flex-wrap items-start justify-between gap-3 sm:flex-nowrap">
        <div className="flex w-full flex-wrap gap-3 sm:gap-5">
          <div className="flex w-full">
            <span className="mr-2 mt-1 flex h-4 w-full max-w-4 items-center justify-center rounded-full border border-primary">
              <span className="block h-2.5 w-full max-w-2.5 rounded-full bg-primary"></span>
            </span>
            <div className="w-full">
              <p className="text-xl font-semibold text-primary">
                Total Flights
              </p>
              <p className="text-md font-medium">01.01.2024 - 31.12.2024</p>
            </div>
          </div>
        </div>
      </div>

      <div>
        <div id="chartOne" className="-ml-5">
          <ReactApexChart
            options={options}
            series={state.series}
            type="area"
            height={350}
            width={"100%"}
          />
        </div>
      </div>
    </div>
  );
};

export default ChartOne;
